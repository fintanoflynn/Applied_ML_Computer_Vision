def humanise(raw_label: str) -> tuple[str, str]:
    """Split a raw PlantVillage label into (plant, condition) parts.

    Example: ``"Tomato___Late_blight"`` -> ``("Tomato", "Late blight")``.
    """
    plant, _, condition = raw_label.partition("___")
    plant = plant.replace("_", " ").strip()
    condition = condition.replace("_", " ").strip() or "unknown"
    return plant, condition
